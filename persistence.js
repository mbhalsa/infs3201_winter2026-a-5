/**
 * Increase failed login attempts by 1.
 * @param {string} username
 * @returns {number} updated failed attempts
 */
async function incrementFailedLogin(username) {
    let db = await getDatabase()
    let users = db.collection('users')

    await users.updateOne(
        { user: username },
        { $inc: { failedLoginAttempts: 1 } }
    )

    let updatedUser = await users.findOne({ user: username })
    return updatedUser.failedLoginAttempts
}

/**
 * Reset failed login attempts to 0.
 * @param {string} username
 */
async function resetFailedLogin(username) {
    let db = await getDatabase()
    let users = db.collection('users')

    await users.updateOne(
        { user: username },
        { $set: { failedLoginAttempts: 0 } }
    )
}

/**
 * Lock a user account.
 * @param {string} username
 */
async function lockUserAccount(username) {
    let db = await getDatabase()
    let users = db.collection('users')

    await users.updateOne(
        { user: username },
        { $set: { disabled: true } }
    )
}
