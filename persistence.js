async function checkCredentials(username, password) {
    let hashedPassword = crypto.createHash('sha256')
        .update(password)
        .digest('hex')
    let db = await getDatabase()
    let users = db.collection('users')
    let result = await users.findOne({
        user: username,
        password: hashedPassword
    })
    return result != null
}

/**
 * Create a new session given the session id (generated by the above layer).  The session object
 * in the database will have an id and an expiry field... data can be arbitrary.
 * @param {*} sessionId 
 * @param {*} timeout 
 * @param {*} data 
 */
async function createSession(sessionId, timeout, data) {
    let db = await getDatabase()
    let sessions = db.collection('sessions')
    const expiresAt = new Date(Date.now() + timeout * 1000)
    await sessions.insertOne({
        id: sessionId,
        expiry: expiresAt,
        data: data
    })
}

/**
 * Get the data associated with a session.
 * @param {*} sessionId 
 * @returns The data associated with a session or null if the data or session does not exist.
 */
async function getSessionData(sessionId) {
    let db = await getDatabase()
    let sessions = db.collection('sessions')
    let result = await sessions.findOne({
        id: sessionId
    })
    if (result) {
        return result.data
    }
    return null
}

/**
 * Update the timestamp in the database for the session.
 * 
 * @param {*} sessionId 
 * @param {*} seconds 
 * @returns true if the operation was successful.
 */
async function extendSession(sessionId, seconds) {
    let db = await getDatabase()
    let sessions = db.collection('sessions')
    let newExpiry = new Date(Date.now() + seconds*1000)
    let result = await sessions.updateOne({id: sessionId}, {$set: {expiry: newExpiry}})
    return result.modifiedCount === 1
}

/**
 * Record an event to the security log
 * 
 * @param {*} username 
 * @param {*} url 
 * @param {*} method 
 */
async function logEvent(username, url, method) {
    let db = await getDatabase()
    let securityLog = db.collection('security_log')
    let timestamp = new Date(Date.now())
    securityLog.insertOne({
        timestamp,
        username,
        url,
        method
    })
}
